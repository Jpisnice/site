export function Logo({ isHovered = false, className = '' }: LogoProps) {
  const svgRef = useRef<SVGSVGElement>(null)
  const pathRefs = useRef<(SVGPathElement | null)[]>([])

  // Entrance: pencil draw-in, then fill solid letters
  useGSAP(
    () => {
      const paths = pathRefs.current.filter(Boolean) as SVGPathElement[]
      if (paths.length === 0) return

      paths.forEach((path) => {
        const len = path.getTotalLength()
        gsap.set(path, {
          strokeDasharray: len,
          strokeDashoffset: len,
          fillOpacity: 0,
          stroke: 'currentColor',
          strokeWidth: AI_STROKE,
        })
      })

      const tl = gsap.timeline()

      // Draw each letter sequentially with stagger
      paths.forEach((path, i) => {
        tl.to(
          path,
          { strokeDashoffset: 0, duration: 0.5, ease: 'power2.inOut' },
          i * 0.1,
        )
      })

      // After drawing completes, fill solid letters and fade their stroke out
      const solidPaths = paths.filter((_, i) => !LETTERS[i].ai)
      tl.to(
        solidPaths,
        { fillOpacity: 1, duration: 0.3, ease: 'power2.out' },
        '>-0.15',
      ).to(
        solidPaths,
        { strokeWidth: 0, duration: 0.25, ease: 'power2.out' },
        '<',
      )

      return () => tl.kill()
    },
    { scope: svgRef },
  )

  // Hover: fill / drain the "ai" letters
  useGSAP(
    () => {
      const aiPaths = pathRefs.current.filter(
        (p, i) => p !== null && LETTERS[i].ai,
      ) as SVGPathElement[]
      if (aiPaths.length === 0) return

      gsap.to(aiPaths, {
        fillOpacity: isHovered ? 1 : 0,
        duration: isHovered ? 0.35 : 0.45,
        ease: isHovered ? 'power2.out' : 'power2.in',
      })
    },
    { dependencies: [isHovered], scope: svgRef },
  )

  return (
    <svg
      ref={svgRef}
      viewBox="-0.5 11.2 143 39.1"
      fill="none"
      xmlns="http://www.w3.org/2000/svg"
      className={className}
      preserveAspectRatio="xMidYMid meet"
      shapeRendering="geometricPrecision"
      overflow="visible"
      aria-hidden
    >
      {LETTERS.map((letter, i) => (
        <path
          key={letter.id}
          ref={(el) => {
            pathRefs.current[i] = el
          }}
          d={letter.d}
          fill="currentColor"
          strokeLinecap="round"
          strokeLinejoin="round"
        />
      ))}
    </svg>
  )
}
